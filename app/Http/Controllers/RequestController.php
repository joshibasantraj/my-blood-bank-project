    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules =[
            'email'=>'required|email|unique:requesters,email',
            'mobile'=>'required|string',
            'date'=>'required|date'

        ];
        $request->validate($rules);

        $data=$request->except('_token');
       $data['created_at']=now();
        $success= DB::table('requesters')->insert($data);

        if($success){
            return redirect()->route('homepage')->with('alert','Request added successfully');
        }else{
            return redirect()->route('homepage')->with('alert','Request not added at this time');
        }
    }
